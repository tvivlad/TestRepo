using System;

namespace Calc
{
    public class Calcul
    {
        public int sum(int x, int y)
        {
            return x + y; 
        }
        public int dif(int x, int y)
        {
            return x - y;
        }

        public int mult(int x, int y)
        {
            return x * y; 
        }
    }
}
